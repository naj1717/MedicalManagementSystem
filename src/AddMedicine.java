    public void loadMedicineNamesToComboBox(){
        try {
            Connection con = ConnectionProvider.getCon();
            String sql = "SELECT DISTINCT name FROM medicine ORDER BY name";
            PreparedStatement pst = con.prepareStatement(sql);
            ResultSet rs = pst.executeQuery();

            jComboBox.removeAllItems(); // Clear combo

            boolean hasData = false;
            while (rs.next()) {
                String medName = rs.getString("name");
                System.out.println("Found: " + medName); // 👈 Debug output
                jComboBox.addItem(medName);
                hasData = true;
            }

            if (!hasData) {
                System.out.println("No medicine names found!");
            }

            
        } catch (Exception e) {
            e.printStackTrace(); // Print exact error
        }

    }

