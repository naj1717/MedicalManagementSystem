    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        
        
        
        SimpleDateFormat dFormat = new SimpleDateFormat("dd-MM-yyyy");
        String mdate = "";
        if(mfgDate !=null && mfgDate.getDate() !=null)
        {
            mdate = dFormat.format(mfgDate.getDate());
        }
        
        String edate = "";
        if(expDate !=null && expDate.getDate()!=null)
        {
            edate = dFormat.format(expDate.getDate());
        }
        
        String name = txtName.getText();
        String companyName = txtCompanyName.getText();
        String quantity = txtQuantity.getText();
        String price = txtPricePerUnit.getText();
        
        if(name.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Name field is required.");
        }
        else if(companyName.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Company Name field is required.");
        }
        else if(quantity.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Quantity field is required.");
        }
        else if(!quantity.matches(numberPattern))
        {
            JOptionPane.showMessageDialog(null, "Quantity field is Invalid.");
        }
        else if(price.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Price Per Unit field is required.");
        }
        else if(!price.matches(numberPattern))
        {
            JOptionPane.showMessageDialog(null, "Price Per Unit field is Invalid.");
        }
        else if(mdate.equals(""))
        {
            JOptionPane.showMessageDialog(null, "mfgDate field is required.");
        }
        else if(edate.equals(""))
        {
            JOptionPane.showMessageDialog(null, "expDate field is required.");
        }
        else
        {
            
            try{
            Connection con =ConnectionProvider.getCon();
            String checkQuery = "SELECT * FROM medicine WHERE name = ?";
            PreparedStatement checkStmt = con.prepareStatement(checkQuery);
            checkStmt.setString(1, name);
            ResultSet rsCheck = checkStmt.executeQuery();
            if (rsCheck.next()) {  // If a record exists, show an error
                JOptionPane.showMessageDialog(null, "Medicine already exists!", "Duplicate Entry", JOptionPane.ERROR_MESSAGE);
            } else {
        // 2️⃣ Get max(uniqueId) and increment it
            int newuniqueId = 1;
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("SELECT MAX(uniqueId) FROM medicine");
            if (rs.next()) {
                 newuniqueId = rs.getInt(1) + 1;
            }
            String insertQuery = "INSERT INTO medicine (uniqueId, name, companyName, quantity, price, mdate, edate) VALUES (?, ?, ?, ?, ?, ?, ?)";
            PreparedStatement ps = con.prepareStatement(insertQuery);
           
            ps.setInt(1, newuniqueId);
            ps.setString(2, name);
            ps.setString(3, companyName);
            ps.setString(4, quantity);
            ps.setString(5, price);
            ps.setString(6, mdate);
            ps.setString(7, edate);
            ps.executeUpdate();
            JOptionPane.showMessageDialog(null, "Medicine Added Successfully");
            setVisible(false);
            new AddMedicine().setVisible(true);
            }
            
            }catch(Exception e)
            {
                JOptionPane.showMessageDialog(null, e);
            }
        
    }//GEN-LAST:event_jButton2ActionPerformed
    }
